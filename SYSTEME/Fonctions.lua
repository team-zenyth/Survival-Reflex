function Credits()

    while true do 
    
        screen:clear()
        pad = Controls.read()

        screen:fontPrint( police, Production.x, Production.y, Production.texte, blanc) 
        screen:fontPrint( police, Projet.x, Projet.y, Projet.texte, blanc)
        screen:fontPrint( police, Codeur.x, Codeur.y, Codeur.texte, blanc) 
        screen:fontPrint( police, Graphisme.x, Graphisme.y, Graphisme.texte, blanc) 
        screen:fontPrint( police, Remerci.x, Remerci.y, Remerci.texte, blanc) 
        screen:fontPrint( police, Conclusion.x, Conclusion.y, Conclusion.texte, blanc)

  
        Production.y = Production.y - monte
        Projet.y = Projet.y - monte
        Codeur.y = Codeur.y - monte
        Graphisme.y = Graphisme.y - monte
        Remerci.y = Remerci.y - monte
        Conclusion.y = Conclusion.y - monte

        if pad:cross() and not oldpad:cross()  then
            monte = monte + 1
        end
        if monte > 6 then
            monte = 2
        end


        if Production.y < -30 then
            Production.y = 273
        elseif Projet.y < -30 then
            Projet.y = 273
        elseif Codeur.y < -30 then
            Codeur.y = 273
        elseif Graphisme.y < -30 then
            Graphisme.y = 273
        elseif Remerci.y < -30 then
            Remerci.y = 273
        elseif Conclusion.y < -30 then
            Conclusion.y = 273
        end

        if pad:circle() and not oldpad:circle()  then
            break
        end
        
        screen.waitVblankStart()
        screen.flip()

    end

end

