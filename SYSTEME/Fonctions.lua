function Splash()

while true do

    screen:clear()
        if logo.l < 0 and face == 0 then
        face = 1
    elseif zenyth.l > 10 and face == 1 then
        face = 2
    elseif zenyth.l < 0 and face == 2 then
        face = 3
    elseif logo.l > 10 and face == 3 then
        nfois = nfois + 1
        face = 0
    end

    if face == 0 then
        partie1()
        logo.x = logo.x+v/2
        logo.l = logo.l-v
    elseif face == 1 then
        partie2()
        zenyth.x = zenyth.x-v/2
        zenyth.l = zenyth.l+v
    elseif face == 2 then
        partie2()
        zenyth.x = zenyth.x + v/2
        zenyth.l = zenyth.l-v
    elseif face == 3 then
        partie1()
        logo.x = logo.x-v/2
        logo.l = logo.l+v
    end

    if nfois >= 2 then
        break
    end
    screen.flip()
    screen.waitVblankStart()
end

end

------------------*Aide*------------------

function Aide()

    while true do 
    
        screen:clear()
        pad = Controls.read()
        screen:blit(0,0,aide) --Faire l'aide en horizon
        
        if pad:circle() and not oldpad:circle() then
            break
        end
        
        oldpad = pad
        screen.flip()
        screen.waitVblankStart()
    
    end

end

